    """üìå –í—ã–±–∏—Ä–∞–µ—Ç —Å–ª–µ–¥—É—é—â—É—é —Ç–µ–º—É, –∏—Å–∫–ª—é—á–∞—è —É–∂–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–Ω—ã–µ."""
